/**
 * Validate that all images are unique
 */
export function validateUniqueImages(): { isValid: boolean; duplicates: string[] } {
  const urls = Object.values(therapyImagesUnique);
  const seen = new Set<string>();
  const duplicates: string[] = [];
  
  for (const url of urls) {
    // Extract just the photo ID to compare
    const photoId = url.split('?')[0];
    if (seen.has(photoId)) {
      duplicates.push(url);
    }
    seen.add(photoId);
  }
  
  return {
    isValid: duplicates.length === 0,
    duplicates
  };
}

