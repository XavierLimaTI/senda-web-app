export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>('pt')
  const [mounted, setMounted] = useState(false)

  // Carregar idioma salvo do localStorage apenas no client
  useEffect(() => {
    setMounted(true)
    if (typeof window !== 'undefined') {
      const saved = localStorage.getItem('language') as Language
      if (saved && Object.keys(translations).includes(saved)) {
        setLanguageState(saved)
      }
    }
  }, [])

  const setLanguage = (lang: Language) => {
    setLanguageState(lang)
    if (typeof window !== 'undefined') {
      localStorage.setItem('language', lang)
    }
  }

  const t = (key: string, params?: Record<string, string | number>): string => {
    // Busca direta primeiro (chaves com pontos como 'auth.signin.welcome')
    let translation = (translations[language] as Record<string, any>)[key]
    if (translation) {
      // Substituir variáveis {var} se params fornecido
      if (params) {
        Object.keys(params).forEach(paramKey => {
          translation = translation.replace(new RegExp(`\\{${paramKey}\\}`, 'g'), String(params[paramKey]))
        })
      }
      return translation
    }
    
    // Fallback para busca aninhada (se necessário no futuro)
    const keys = key.split('.')
    let current: any = translations[language]
    
    for (const k of keys) {
      if (current && typeof current === 'object' && k in current) {
        current = current[k]
      } else {
        return key // Retorna a chave se não encontrar
      }
    }
    
    if (typeof current === 'string') {
      // Substituir variáveis {var} se params fornecido
      if (params) {
        Object.keys(params).forEach(paramKey => {
          current = current.replace(new RegExp(`\\{${paramKey}\\}`, 'g'), String(params[paramKey]))
        })
      }
      return current
    }
    
    return key
  }

  // Evitar flash de conteúdo não traduzido
  if (!mounted) {
    return null
  }

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    return {
      language: 'pt' as Language,
      setLanguage: () => {},
      t: (key: string, params?: Record<string, string | number>) => key,
    }
  }
  return context
}
